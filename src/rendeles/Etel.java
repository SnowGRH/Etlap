
package rendeles;


public class Etel {
    private String neve;
    private int ar;

    public Etel(String neve, int ar) {
        this.neve = neve;
        this.ar = ar;
    }

    public String getNeve() {
        return neve;
    }

    public int getAr() {
        return ar;
    }

    public void setNeve(String neve) {
        this.neve = neve;
    }

    public void setAr(int ar) {
        this.ar = ar;
    }
    
}
